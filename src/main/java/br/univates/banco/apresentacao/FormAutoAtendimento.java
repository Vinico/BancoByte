    private void atualizarTransacoes(){
        jPanel2.removeAll();

        // Pega a lista de transações da conta
        ArrayList<Transacao> lista = conta.getListaTransacoes();

        if (lista != null && !lista.isEmpty()) {
            for (Transacao t : lista) {
                JLabel lbl = new JLabel(t.getTransacaoFormatada());
                jPanel2.add(lbl);
            }
        } else {
            jPanel2.add(new JLabel("Nenhuma transação registrada"));
        }

        // Atualiza o layout do painel
        jPanel2.revalidate();
        jPanel2.repaint();
        
        
    
    }

