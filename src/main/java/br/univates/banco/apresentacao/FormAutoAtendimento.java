    private void btnTransferirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransferirActionPerformed
        String valor = JOptionPane.showInputDialog("Digite o valor a ser transferido");
        IDao<ContaBancaria, Integer> daoConta = DaoFactory.createContaBancariaDao();
        
        try {
            conta.fazerPix(Integer.valueOf(valor));
            
            lblSaldo.setText("R$" + conta.getSaldo());
            daoConta.update(conta);
            atualizarTransacoes();
            
        } catch (ValorNegativoException ex) {
            System.getLogger(FormAutoAtendimento.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        } catch (SaldoInsuficienteException ex) {
            System.getLogger(FormAutoAtendimento.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        } catch (RecordNotFoundException ex) {
            System.getLogger(FormAutoAtendimento.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_btnTransferirActionPerformed

    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        String valor = JOptionPane.showInputDialog("Digite o valor a ser depositado");
        IDao<ContaBancaria, Integer> daoConta = DaoFactory.createContaBancariaDao();
        try {
            conta.depositar(Integer.valueOf(valor));
            daoConta.update(conta);
            lblSaldo.setText("R$" + conta.getSaldo());
            atualizarTransacoes();
        } catch (ValorNegativoException ex) {
            System.getLogger(FormAutoAtendimento.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        } catch (RecordNotFoundException ex) {
            System.getLogger(FormAutoAtendimento.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        
    }//GEN-LAST:event_btnDepositarActionPerformed

