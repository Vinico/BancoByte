    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         IDao<Correntista, String> daoCorre = DaoFactory.createCorrentistaDao();
        try {
            daoCorre.update(new Correntista(new Cpf(txtCPF.getText(), false), 
                    txtNome.getText(), 
                    txtMuni.getText()));
            
            JOptionPane.showMessageDialog(this, "Correntista atualizado", "Message", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (RecordNotFoundException e) {
            throw new RuntimeException(e);
        } catch (InvalidEntryException e) {
            throw new RuntimeException(e);
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

