    public FormEditarCorrentista(String cpf) {
        initComponents();
        txtCPF.setText(cpf);
        txtCPF.setEditable(false);
        txtCPF.setEnabled(false);
        IDao<Correntista, String> daoCorre = DaoFactory.createCorrentistaDao();

        Correntista corre = null;
        try {
            corre = daoCorre.read(cpf);
        } catch (RecordNotFoundException e) {
            throw new RuntimeException(e);
        }
        txtNome.setText(corre.getNome());
        txtMuni.setText(corre.getMunicipio());
    }

